#include"libft.h"

int	ft_isalnum(int c)
{
	if ('A' <= c && c <= 'Z')
		return (1);
	else if ('a' <= c && c <= 'z')
		return (2);
	else if ('0' <= c && c <= '9')
		return (4);
	else
		return (0);
}
